// context/ConverteContext.jsx
import React, { createContext, useState, useEffect, useMemo, useCallback } from "react";
import axios from "axios";
import { toast } from "react-hot-toast";
import { handleEpubFile, checkFileFormatFromText, handleTxtFile } from "../utils/fileHandlers";
import { API_URL } from '../config/config';


// Helper function ƒë·ªÉ l·∫•y token
export const getAuthToken = () => localStorage.getItem("auth-token");

export const AuthContext = createContext();

export const AuthProvider = ({ children }) => {
  // ===== STATE MANAGEMENT =====
  const [isLoggedIn, setIsLoggedIn] = useState(false);
  const [menu, setMenu] = useState("home");
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState(null);

  // S·ª≠ d·ª•ng useMemo ƒë·ªÉ cache token
  const token = useMemo(() => {
    return localStorage.getItem("auth-token");
  }, [isLoggedIn]); // Ch·ªâ t√≠nh to√°n l·∫°i khi isLoggedIn thay ƒë·ªïi

  // ===== USER STATE & FUNCTIONS =====
  const [userData, setUserData] = useState({
    id: "",
    username: "",
    email: "",
    avatar: "",
    backgroundImage: "",
    birthdate: "",
    libraryStories: [],
    userApiKeys: [],
    createdAt: "",
    updatedAt: "",
  });

  // User Authentication
  useEffect(() => {
    if (token) {
      setIsLoggedIn(true);
      fetchUserData();
    }
  }, [token]);

  // üöÄ Th√™m useEffect ƒë·ªÉ √°p d·ª•ng background ngay khi app kh·ªüi ƒë·ªông
  useEffect(() => {
    if (userData.backgroundImage) {
      const bgImage = `http://localhost:8000/data/upload/background/${userData.backgroundImage}`;
      document.body.style.backgroundImage = `url(${bgImage})`;
      document.body.style.backgroundSize = "cover";
      document.body.style.backgroundPosition = "center";
      document.body.style.backgroundAttachment = "fixed";
      localStorage.setItem("backgroundImage", bgImage);
      console.log(`[BACKGROUND] ƒê√£ √°p d·ª•ng background: ${bgImage}`);
    } else {
      // Ch·ªâ x√≥a background n·∫øu user kh√¥ng ƒëƒÉng nh·∫≠p ho·∫∑c kh√¥ng c√≥ background
      if (!userData.id) {
        document.body.style.backgroundImage = "";
        localStorage.removeItem("backgroundImage");
        console.log('[BACKGROUND] ƒê√£ x√≥a background v√¨ user kh√¥ng ƒëƒÉng nh·∫≠p');
      }
    }
  }, [userData.backgroundImage, userData.id]);

  const onLogin = (userData) => {
    if (!userData) return;
    setIsLoggedIn(true);
    setUserData({
      id: userData.id || "",
      username: userData.username || "",
      email: userData.email || "",
      avatar: userData.avatar || "",
      backgroundImage: userData.backgroundImage || "",
      birthdate: userData.birthdate || "",
      libraryStories: userData.libraryStories || [],
      userApiKeys: userData.userApiKeys || [],
      createdAt: userData.createdAt || "",
      updatedAt: userData.updatedAt || "",
    });
  };

  const onLogout = () => {
    localStorage.removeItem("auth-token");
    setIsLoggedIn(false);
    // üöÄ X√≥a background khi logout
    document.body.style.backgroundImage = "";
    localStorage.removeItem("backgroundImage");
    console.log('[BACKGROUND] ƒê√£ x√≥a background khi logout');
    setUserData({
      id: "",
      username: "",
      email: "",
      avatar: "",
      backgroundImage: "",
      birthdate: "",
      libraryStories: [],
      userApiKeys: [],
      createdAt: "",
      updatedAt: "",
    });
  };

  // User Profile Management
  const fetchUserData = async () => {
    try {
      setLoading(true);
      const response = await axios.get(`${API_URL}/user/profile`, {
        headers: { Authorization: `Bearer ${token}` },
      });
      if (response.data) {
        setUserData({
          id: response.data.id,
          username: response.data.username,
          email: response.data.email,
          avatar: response.data.avatar || "",
          backgroundImage: response.data.backgroundImage || "",
          birthdate: response.data.birthdate || "",
          libraryStories: response.data.libraryStories || [],
          userApiKeys: response.data.UserApiKey || [],
          createdAt: response.data.createdAt,
          updatedAt: response.data.updatedAt,
        });
      }
    } catch (err) {
      console.error("L·ªói khi l·∫•y th√¥ng tin user:", err);
      setError(err.message);
    } finally {
      setLoading(false);
    }
  };

  const updateUserProfile = async (updateData) => {
    try {
      setLoading(true);

      if (updateData.newPassword) {
        const response = await axios.put(
          `${API_URL}/user/change-password`,
          updateData,
          {
            headers: { Authorization: `Bearer ${token}` },
          }
        );
        await fetchUserData();
        return response.data;
      } else {
        const response = await axios.put(
          `${API_URL}/user/profile`,
          updateData,
          {
            headers: { Authorization: `Bearer ${token}` },
          }
        );

        if (response.data) {
          setUserData((prev) => ({
            ...prev,
            ...response.data,
          }));
          await fetchUserData();
        }
        return response.data;
      }
    } catch (err) {
      console.error("L·ªói khi c·∫≠p nh·∫≠t th√¥ng tin:", err);
      setError(err.message);
      throw err;
    } finally {
      setLoading(false);
    }
  };

  const updateAvatar = async (formData) => {
    try {
      setLoading(true);
      const response = await axios.post(
        `${API_URL}/upload/image/avatar`,
        formData,
        {
          headers: {
            Authorization: `Bearer ${token}`,
            "Content-Type": "multipart/form-data",
          },
        }
      );

      if (response.data) {
        setUserData((prev) => ({
          ...prev,
          avatar: response.data.filePath || response.data.data.filePath,
        }));
        await fetchUserData();
      }
      return response.data;
    } catch (err) {
      console.error("L·ªói khi c·∫≠p nh·∫≠t avatar:", err);
      setError(err.message);
      throw err;
    } finally {
      setLoading(false);
    }
  };

  const updateBackground = async (formData) => {
    try {
      setLoading(true);
      const response = await axios.post(
        `${API_URL}/upload/image/background`,
        formData,
        {
          headers: {
            Authorization: `Bearer ${token}`,
            "Content-Type": "multipart/form-data",
          },
        }
      );

      if (response.data) {
        setUserData((prev) => ({
          ...prev,
          backgroundImage: response.data.data.filePath,
        }));
      }
      return response.data;
    } catch (err) {
      console.error("L·ªói khi c·∫≠p nh·∫≠t background:", err);
      setError(err.message);
      throw err;
    } finally {
      setLoading(false);
    }
  };

  // ===== API KEY MANAGEMENT =====
  const addApiKey = async (keyData) => {
    try {
      setLoading(true);
      const response = await axios.post(
        `${API_URL}/user/keys`,
        keyData,
        {
          headers: { Authorization: `Bearer ${token}` },
        }
      );

      if (response.data) {
        setUserData((prev) => ({
          ...prev,
          userApiKeys: [...prev.userApiKeys, response.data],
        }));
      }
      return response.data;
    } catch (err) {
      console.error("L·ªói khi th√™m API key:", err);
      setError(err.message);
      throw err;
    } finally {
      setLoading(false);
    }
  };

  const removeApiKey = async (keyId) => {
    try {
      setLoading(true);
      await axios.delete(`${API_URL}/user/keys/${keyId}`, {
        headers: { Authorization: `Bearer ${token}` },
      });

      setUserData((prev) => ({
        ...prev,
        userApiKeys: prev.userApiKeys.filter((key) => key.id !== keyId),
      }));
    } catch (err) {
      console.error("L·ªói khi x√≥a API key:", err);
      setError(err.message);
      throw err;
    } finally {
      setLoading(false);
    }
  };

  const [userApiKey, setUserApiKey] = useState([]);

  const fetchApiKey = async () => {
    try {
      setLoading(true);
      const response = await axios.get(`${API_URL}/user/keys`, {
        headers: {
          Authorization: `Bearer ${token}`,
        },
      });

      setUserApiKey(response.data);

      setError(null);
    } catch (err) {
      console.error("L·ªói khi t·∫£i danh s√°ch API key:", err);
      setError(
        "L·ªói khi t·∫£i danh s√°ch API key: " +
          (err.response?.data?.error || err.message)
      );
    } finally {
      setLoading(false);
    }
  };

  // ===== STORY MANAGEMENT =====
  const [stories, setStories] = useState([]);
 

  const fetchStories = async () => {
    try {
      setLoading(true);
      const response = await axios.get(`${API_URL}/user/library`, {
        headers: {
          Authorization: `Bearer ${token}`,
        },
      });
      setStories(response.data);
      setError(null);
    } catch (err) {
      console.error("L·ªói khi t·∫£i danh s√°ch truy·ªán:", err);
      setError(
        "L·ªói khi t·∫£i danh s√°ch truy·ªán: " +
          (err.response?.data?.error || err.message)
      );
    } finally {
      setLoading(false);
    }
  };

  const createStory = async (file, storyInfo) => {
    try {
      setLoading(true);
      if (!token) {
        throw new Error("Kh√¥ng t√¨m th·∫•y token x√°c th·ª±c");
      }

      // Upload v√† x·ª≠ l√Ω file tr∆∞·ªõc
      const uploadResult = await uploadFile(file);
      const { chapters } = uploadResult;

      // T·∫°o truy·ªán v·ªõi th√¥ng tin chapters
      const response = await axios.post(
        `${API_URL}/user/library`,
        {
          ...storyInfo,
          chapters: chapters
        },
        {
          headers: {
            Authorization: `Bearer ${token}`,
          },
        }
      );

      return response.data;
    } catch (err) {
      console.error("L·ªói khi t·∫°o truy·ªán m·ªõi:", err);
      setError(err.message);
      throw err;
    } finally {
      setLoading(false);
    }
  };

  const editStories = async (storyId, field, value) => {
    try {
      const response = await axios.put(
        `${API_URL}/user/library/${storyId}`,
        { [field]: value },
        {
          headers: {
            Authorization: `Bearer ${token}`,
          },
        }
      );

      setStories((prevStories) =>
        prevStories.map((story) =>
          story.id === storyId ? { ...story, [field]: value } : story
        )
      );

      if (field === "storyAvatar") {
        setUserData((prev) => ({
          ...prev,
          libraryStories: prev.libraryStories.map((story) =>
            story.id === storyId ? { ...story, [field]: value } : story
          ),
        }));
      }

      return response.data;
    } catch (err) {
      console.error("L·ªói khi c·∫≠p nh·∫≠t truy·ªán:", err);
      setError(
        "L·ªói khi c·∫≠p nh·∫≠t truy·ªán: " + (err.response?.data?.error || err.message)
      );
      throw err;
    }
  };

  const hideStories = async (storyId) => {
    try {
      const response = await axios.patch(
        `${API_URL}/user/library/${storyId}/hide`,
        {},
        {
          headers: {
            Authorization: `Bearer ${token}`,
          },
        }
      );
      if (response.status === 200) {
        fetchStories();
        toast.success("ƒê√£ xo√° truy·ªán th√†nh c√¥ng");
      }
    } catch (error) {
      console.error("Error hiding story:", error);
      toast.error("L·ªói khi xo√° truy·ªán");
    }
  };

  const deleteStories = async (storyId) => {
    try {
      const response = await axios.delete(`${API_URL}/user-library/${storyId}`, {
        headers: {
          Authorization: `Bearer ${token}`,
        },
      });
      if (response.status === 200) {
        setStories((prevStories) =>
          prevStories.filter((story) => story.id !== storyId)
        );
        setUserData((prev) => ({
          ...prev,
          libraryStories: prev.libraryStories.filter(
            (story) => story.id !== storyId
          ),
        }));
        toast.success("ƒê√£ x√≥a truy·ªán vƒ©nh vi·ªÖn");
      }
    } catch (error) {
      console.error("Error deleting story:", error);
      toast.error("L·ªói khi x√≥a truy·ªán");
    }
  };

  // ===== FILE MANAGEMENT =====
  const uploadFile = async (file) => {
    try {
      setLoading(true);
      if (!token) {
        throw new Error("Kh√¥ng t√¨m th·∫•y token x√°c th·ª±c");
      }

      // ƒê·ªçc file b·∫±ng FileReader
      const reader = new FileReader();
      const fileContent = await new Promise((resolve, reject) => {
        reader.onload = () => resolve(reader.result);
        reader.onerror = reject;
        reader.readAsText(file);
      });

      let chapters = [];
      const setChaptersCallback = (chaps) => {
        chapters = chaps;
      };

      // X·ª≠ l√Ω file d·ª±a v√†o ƒë·ªãnh d·∫°ng
      const fileExt = file.name.split('.').pop().toLowerCase();
      if (fileExt === 'epub') {
        await handleEpubFile(
          fileContent,
          setChaptersCallback,
          (error) => { throw new Error(error); },
          () => {}, // setSuccess
          () => {}, // setChapterCount
          () => {}, // setTotalWords
          () => {}, // setAverageWords
          () => {}, // setBooks
          () => {}  // setAuthor
        );
      } else if (fileExt === 'txt') {
        handleTxtFile(
          fileContent,
          setChaptersCallback,
          (error) => { throw new Error(error); },
          () => {}, // setSuccess
          { current: null }, // fileInputRef
          () => {}, // setSelectedFile
          file,
          () => {}, // setChapterCount
          () => {}, // setTotalWords
          () => {}, // setAverageWords
          () => {}, // setBooks
          () => {}  // setAuthor
        );
      } else {
        throw new Error('ƒê·ªãnh d·∫°ng file kh√¥ng ƒë∆∞·ª£c h·ªó tr·ª£');
      }

      if (!chapters || chapters.length === 0) {
        throw new Error('Kh√¥ng th·ªÉ ƒë·ªçc ƒë∆∞·ª£c n·ªôi dung ch∆∞∆°ng t·ª´ file');
      }

      // G·ª≠i chapters l√™n server
      const response = await axios.post(
        `${API_URL}/upload`,
        {
          fileName: file.name,
          chapters: chapters
        },
        {
          headers: {
            Authorization: `Bearer ${token}`,
            'Content-Type': 'application/json'
          },
        }
      );

      return { ...response.data, chapters };
    } catch (err) {
      console.error("L·ªói khi upload file:", err);
      setError(err.message);
      throw err;
    } finally {
      setLoading(false);
    }
  };

  const processFile = async (fileId) => {
    try {
      setLoading(true);
      if (!token) {
        throw new Error("Kh√¥ng t√¨m th·∫•y token x√°c th·ª±c");
      }

      const response = await axios.post(
        `${API_URL}/process/file/${fileId}`,
        {},
        {
          headers: {
            Authorization: `Bearer ${token}`,
          },
        }
      );
      return response.data;
    } catch (err) {
      console.error("L·ªói khi x·ª≠ l√Ω file:", err);
      setError(err.message);
      throw err;
    } finally {
      setLoading(false);
    }
  };

  const getProcessedFile = async (fileId) => {
    try {
      setLoading(true);
      if (!token) {
        throw new Error("Kh√¥ng t√¨m th·∫•y token x√°c th·ª±c");
      }

      const response = await axios.get(
        `${API_URL}/process/file/${fileId}`,
        {
          headers: {
            Authorization: `Bearer ${token}`,
          },
        }
      );
      return response.data;
    } catch (err) {
      console.error("L·ªói khi l·∫•y th√¥ng tin file ƒë√£ x·ª≠ l√Ω:", err);
      setError(err.message);
      throw err;
    } finally {
      setLoading(false);
    }
  };

  // ===== CHAPTER MANAGEMENT =====
  const addChapter = async (chapterData) => {
    try {
      setLoading(true);
      if (!token) {
        throw new Error("Kh√¥ng t√¨m th·∫•y token x√°c th·ª±c");
      }
  
      const response = await axios.post(
        `${API_URL}/user/library/${chapterData.storyId}/chapters`,
        {
          chapterNumber: chapterData.chapterNumber,
          chapterName: chapterData.chapterName,
          rawText: chapterData.rawText
        },
        {
          headers: {
            Authorization: `Bearer ${token}`,
          },
        }
      );
      return response.data;
    } catch (err) {
      console.error("L·ªói khi th√™m ch∆∞∆°ng:", err);
      setError(err.message);
      throw err;
    } finally {
      setLoading(false);
    }
  };

  const deleteChapter = async (storyId, chapterNumber) => {
    try {
      setLoading(true);
      if (!token) {
        throw new Error("Kh√¥ng t√¨m th·∫•y token x√°c th·ª±c");
      }

      // X√≥a ch∆∞∆°ng
      await axios.delete(
        `${API_URL}/user/library/${storyId}/chapters/${chapterNumber}`,
        {
          headers: {
            Authorization: `Bearer ${token}`,
          },
        }
      );

      // L·∫•y danh s√°ch ch∆∞∆°ng sau khi x√≥a
      const response = await axios.get(
        `${API_URL}/user/library/${storyId}/chapters`,
        {
          headers: {
            Authorization: `Bearer ${token}`,
          },
        }
      );

      // C·∫≠p nh·∫≠t s·ªë th·ª© t·ª± cho c√°c ch∆∞∆°ng sau
      const chapters = response.data;
      const updatePromises = chapters
        .filter(chapter => chapter.chapterNumber > chapterNumber)
        .map(chapter => 
          axios.put(
            `${API_URL}/user/library/${storyId}/chapters/${chapter.chapterNumber}`,
            {
              chapterNumber: chapter.chapterNumber - 1
            },
            {
              headers: {
                Authorization: `Bearer ${token}`,
              },
            }
          )
        );

      await Promise.all(updatePromises);
      return true;
    } catch (err) {
      console.error("L·ªói khi x√≥a ch∆∞∆°ng:", err);
      setError(err.message);
      throw err;
    } finally {
      setLoading(false);
    }
  };

  const updateChapterContent = async (storyId, chapterNumber, translatedTitle, translatedContent, timeTranslation = 0) => {
    try {
      // Ki·ªÉm tra v√† log c√°c tham s·ªë
      console.log("üìù C·∫≠p nh·∫≠t n·ªôi dung ch∆∞∆°ng:", {
        storyId,
        chapterNumber,
        hasTranslatedTitle: !!translatedTitle,
        hasTranslatedContent: !!translatedContent,
        timeTranslation: timeTranslation
      });

      // Ki·ªÉm tra tham s·ªë b·∫Øt bu·ªôc
      if (!storyId) throw new Error("Thi·∫øu storyId");
      if (!chapterNumber) throw new Error("Thi·∫øu chapterNumber");
      
      setLoading(true);
      const response = await axios.put(
        `${API_URL}/user/library/${storyId}/chapters/${chapterNumber}/translation`,
        {
          translatedTitle: translatedTitle || '', // Ti√™u ƒë·ªÅ ƒë√£ d·ªãch
          translatedContent: translatedContent || '', // N·ªôi dung ƒë√£ d·ªãch
          timeTranslation: timeTranslation // üëâ Th√™m th·ªùi gian d·ªãch
        },
        {
          headers: {
            Authorization: `Bearer ${token}`,
          },
        }
      );
      console.log("‚úÖ C·∫≠p nh·∫≠t th√†nh c√¥ng:", response.data);
      return response.data;
    } catch (err) {
      console.error("L·ªói khi c·∫≠p nh·∫≠t n·ªôi dung ch∆∞∆°ng:", err);
      setError(err.message);
      throw err;
    } finally {
      setLoading(false);
    }
  };

  return (
    <AuthContext.Provider
      value={{
        // State
        isLoggedIn,
        menu,
        loading,
        error,
        userData,
        stories,
        userApiKey,
        getAuthToken,

        // User Functions
        onLogin,
        onLogout,
        setMenu,
        updateUserProfile,
        updateAvatar,
        updateBackground,

        // API Key Functions
        addApiKey,
        removeApiKey,
        fetchApiKey,

        // Story Functions
        fetchStories,
        editStories,
        deleteStories,
        hideStories,
        createStory,
        updateChapterContent,

        // File Functions
        uploadFile,
        processFile,
        getProcessedFile,

        // Chapter Functions
        addChapter,
        deleteChapter,
      }}
    >
      {children}
    </AuthContext.Provider>
  );
};
